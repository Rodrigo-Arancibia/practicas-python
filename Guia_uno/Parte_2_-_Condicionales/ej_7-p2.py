# Declare una variable B y le asigne un valor de tipo entero. A continuación,
# muestre un mensaje indicando si el valor de B es positivo o negativo.
# Consideraremos el 0 como positivo.

print("::: 7. ¿Positivo o Negativo?:::")

B = int(input("Ingrese un número entero: "))

if B >= 0:
    print("El número ingresado es POSITIVO.")
else:
    print("El número ingresado es NEGATIVO.")